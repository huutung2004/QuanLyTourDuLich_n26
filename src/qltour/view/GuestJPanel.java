    private void Search_kh_jTextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_Search_kh_jTextFieldKeyReleased
        // TODO add your handling code here:
        ArrayList<KhachHang> searchKh = new ArrayList<>();
        String search_field = Search_kh_jTextField.getText();
        if (search_field.isEmpty()) {
            setTable();
        } else {
            for (KhachHang kh : listKh) {
                if (kh.getID_client().trim().toLowerCase().contains(search_field.trim().toLowerCase()) || kh.getName_client().trim().toLowerCase().contains(search_field.trim().toLowerCase()) || kh.getPhone().trim().toLowerCase().contains(search_field.trim().toLowerCase()) || kh.getCMND().toLowerCase().trim().contains(search_field.trim()) || kh.getSex().trim().toLowerCase().contains(search_field.trim().toLowerCase()) || kh.getHome_town().trim().toLowerCase().contains(search_field.trim().toLowerCase())) {
                    searchKh.add(kh);
                }
            }
            DefaultTableModel defaultTableModel = new DefaultTableModel();
            KhachHang_jTable.setModel(defaultTableModel);
            ListSelectionModel listSelectionModel = KhachHang_jTable.getSelectionModel();
            listSelectionModel.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
            defaultTableModel.addColumn("STT");
            defaultTableModel.addColumn("ID");
            defaultTableModel.addColumn("Tên Khách Hàng");
            defaultTableModel.addColumn("SĐT");
            defaultTableModel.addColumn("CMND/CCCD");
            defaultTableModel.addColumn("Quê Quán");
            defaultTableModel.addColumn("Giới Tính");
            defaultTableModel.addColumn("Email");
            defaultTableModel.addColumn("Ghi Chú");

            int i = 0;
            for (KhachHang khachHang : searchKh) {
                i++;
                defaultTableModel.addRow(new Object[]{i++, khachHang.getID_client(), khachHang.getName_client(), khachHang.getPhone(), khachHang.getCMND(), khachHang.getHome_town(), khachHang.getSex(), khachHang.getEmail(), khachHang.getNotes()});
            }

        }
    }//GEN-LAST:event_Search_kh_jTextFieldKeyReleased

