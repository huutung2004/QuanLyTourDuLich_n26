    private void sort_kh_jComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sort_kh_jComboBoxActionPerformed
        // TODO add your handling code here:
        String sortby = String.valueOf(sort_kh_jComboBox.getSelectedItem());
        if (sortby.equals("ID")) {
            listKh.sort(new Comparator<KhachHang>() {
                @Override
                public int compare(KhachHang o1, KhachHang o2) {
                    return o1.getID_client().compareTo(o2.getID_client());
                }
            });
        } else if (sortby.equals("Tên")) {
            listKh.sort(new Comparator<KhachHang>() {
                @Override
                public int compare(KhachHang o1, KhachHang o2) {
                    return o1.getName_client().compareTo(o2.getName_client());
                }
            });

        } else if (sortby.equals("Quê Quán")) {
            listKh.sort(new Comparator<KhachHang>() {
                @Override
                public int compare(KhachHang o1, KhachHang o2) {
                    return o1.getHome_town().compareTo(o2.getHome_town());
                }
            });
            
        }
        else if(sortby.equals("Giới Tính")){
            listKh.sort(new Comparator<KhachHang>() {
                @Override
                public int compare(KhachHang o1, KhachHang o2) {
                    return o1.getSex().compareTo(o2.getSex());
                }
            });
        }
        else {
            listKh.sort(new Comparator<KhachHang>() {
                @Override
                public int compare(KhachHang o1, KhachHang o2) {
                    return o1.getEmail().compareTo(o2.getEmail());
                }
            });
        }
        o.saveData(listKh, "dsKh.dat");
        setTable();
    }//GEN-LAST:event_sort_kh_jComboBoxActionPerformed

