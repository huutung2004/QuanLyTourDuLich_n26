    private void setTable() {
        DefaultTableModel defaultTableModel = new DefaultTableModel();
        KhachHang_jTable.setModel(defaultTableModel);
        ListSelectionModel listSelectionModel = KhachHang_jTable.getSelectionModel();
        listSelectionModel.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        defaultTableModel.addColumn("STT");
        defaultTableModel.addColumn("ID");
        defaultTableModel.addColumn("Tên Khách Hàng");
        defaultTableModel.addColumn("SĐT");
        defaultTableModel.addColumn("CMND/CCCD");
        defaultTableModel.addColumn("Quê Quán");
        defaultTableModel.addColumn("Giới Tính");
        defaultTableModel.addColumn("Email");
        defaultTableModel.addColumn("Ghi Chú");

        listKh = o.loadData(listKh, "dsKh.dat");
        int i = 0;
        for (KhachHang khachHang : listKh) {
            i++;
            defaultTableModel.addRow(new Object[]{i, khachHang.getID_client(), khachHang.getName_client(), khachHang.getPhone(), khachHang.getCMND(), khachHang.getHome_town(), khachHang.getSex(), khachHang.getEmail(), khachHang.getNotes()});
        }

    }

