    private void add_jButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_jButtonActionPerformed
        // TODO add your handling code here:
        listTour = o.loadData(listTour, "dsTour1.dat");

        double price = 0;
        date_jTextField.setText(String.valueOf(LocalDate.now()));
        String date = date_jTextField.getText();
        String ID = ID_jTextField.getText();
        String name = name_jTextField.getText();
        boolean priceinput = false;
        while (!priceinput) {
            try {
                price = Double.parseDouble(price_jTextField.getText());

                priceinput = true;
            } catch (NumberFormatException e) {
                e.getMessage();
                Tour_jOptionPane.showMessageDialog(add_tour_jDialog, "Price must be number ", "Thông Báo", Tour_jOptionPane.ERROR_MESSAGE);
                break;
            }

        }
        String des = des_jTextField.getText();
        String note = note_jTextArea.getText();
        check_Loi n = new check_Loi();
        int checkID = n.erros_same_IDTour_v1(listTour, ID);
        if (ID.isEmpty()) {
            Tour_jOptionPane.showMessageDialog(add_tour_jDialog, "ID không được rỗng   ", "Thông Báo", Tour_jOptionPane.ERROR_MESSAGE);

        } else if (name.isEmpty()) {
            Tour_jOptionPane.showMessageDialog(add_tour_jDialog, "Name không được rỗng   ", "Thông Báo", Tour_jOptionPane.ERROR_MESSAGE);
        } else if (des.isEmpty()) {
            Tour_jOptionPane.showMessageDialog(add_tour_jDialog, "Địa điểm không được rỗng   ", "Thông Báo", Tour_jOptionPane.ERROR_MESSAGE);
        } else if (checkID == 1) {
            Tour_jOptionPane.showMessageDialog(add_tour_jDialog, "ID đã tồn tại   ", "Thông Báo", Tour_jOptionPane.ERROR_MESSAGE);
        } else {
            Tour ntour = new Tour(ID, name, note, price, date, des);
            listTour.add(ntour);
            Tour_jOptionPane.showMessageDialog(add_tour_jDialog, "Thêm thành công ", "Thông Báo", Tour_jOptionPane.INFORMATION_MESSAGE);

        }
        o.saveData(listTour, "dsTour1.dat");

        setTable();
    }//GEN-LAST:event_add_jButtonActionPerformed

