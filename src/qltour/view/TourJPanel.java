    private void Search_jTextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_Search_jTextFieldKeyReleased
        // TODO add your handling code here:
        ArrayList<Tour> searchTour = new ArrayList<>();
        String search_field = Search_jTextField.getText();
        if (search_field.isEmpty()) {
            setTable();
        } else {
            for (Tour tour : listTour) {
                if (tour.getID_Tour().trim().toLowerCase().contains(search_field.trim().toLowerCase()) || tour.getName_Tour().trim().toLowerCase().contains(search_field.trim().toLowerCase()) || tour.getDestination().trim().toLowerCase().contains(search_field.trim().toLowerCase()) || String.valueOf(tour.getPrice_Tour()).trim().contains(search_field.trim())) {
                    searchTour.add(tour);
                }
            }
            DefaultTableModel defaultTableModel = new DefaultTableModel();
        Tour_jTable.setModel(defaultTableModel);
        ListSelectionModel listSelectionModel = Tour_jTable.getSelectionModel();
        listSelectionModel.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        defaultTableModel.addColumn("STT");
        defaultTableModel.addColumn("ID");
        defaultTableModel.addColumn("Tên Tour");
        defaultTableModel.addColumn("Ngày Tạo");
        defaultTableModel.addColumn("Điểm Đến");
        defaultTableModel.addColumn("Giá");
        defaultTableModel.addColumn("Ghi Chú");
        
        int i = 0;
        for (Tour tour : searchTour) {
            i++;
            defaultTableModel.addRow(new Object[]{i, tour.getID_Tour(), tour.getName_Tour(), tour.getDate_create(), tour.getDestination(), tour.getPrice_Tour(), tour.getNote()});
        }
        }
    }//GEN-LAST:event_Search_jTextFieldKeyReleased


