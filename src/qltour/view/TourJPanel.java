    private void sort_jComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sort_jComboBoxActionPerformed
        // TODO add your handling code here:
        String sortby = String.valueOf(sort_jComboBox.getSelectedItem());
        if (sortby.equals("ID")) {
            listTour.sort(new Comparator<Tour>() {
                @Override
                public int compare(Tour o1, Tour o2) {
                    return o1.getID_Tour().compareTo(o2.getID_Tour());
                }
            });

        } else if (sortby.equals("Name")) {
            listTour.sort(new Comparator<Tour>() {
                @Override
                public int compare(Tour o1, Tour o2) {
                    return o1.getName_Tour().compareTo(o2.getName_Tour());
                }
            });

        } else if (sortby.equals("Date")) {
            listTour.sort(new Comparator<Tour>() {
                @Override
                public int compare(Tour o1, Tour o2) {
                    return o1.getDate_create().compareTo(o2.getDate_create());
                }
            });
        } else {
            listTour.sort(new Comparator<Tour>() {
                @Override
                public int compare(Tour o1, Tour o2) {
                    return (int) (o1.getPrice_Tour() - o2.getPrice_Tour());
                }
            });
        }
        o.saveData(listTour, "dsTour1.dat");
        setTable();
    }//GEN-LAST:event_sort_jComboBoxActionPerformed

